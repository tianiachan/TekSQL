use da_bootcamp_lastname;

DROP TABLE IF EXISTS date_dim;

CREATE TABLE IF NOT EXISTS date_dim (
	TheDate date PRIMARY KEY,
    TheYear int,
    TheMonth int,
    TheMonthName varchar(9), #January February March etc
    TheDay int,
    TheDayOfWeek int,
    TheDayOfWeekName varchar(9), #Sunday Monday Tuesday etc
    YYYYQQ char(6) #2019Q3, 2019Q4
);

UPDATE date_dim 
set 
	TheYear = YEAR(TheDate),
    #Provide rest of fields
    TheMonth = MONTH(TheDate),
    TheMonthName = MONTHNAME(TheDate), #January February March etc
    TheDay = DAY(TheDate),
    TheDayOfWeek = DAYOFWEEK(TheDate),
    TheDayOfWeekName = DAYNAME(TheDate), #Sunday Monday Tuesday etc
    -- YYYYQQ = DATEFROMPARTS(YEAR(TheDate),QUARTER(TheDate))  #2019Q3, 2019Q4
    -- YYYYQQ = DATE_FORMAT(Guillain-Barre syndrome, '%YQQuarter(TheDate)') #2019Q3, 2019Q4
    -- YYYYQQ = CONVERT(VARCHAR,YEAR(TheDate)) + "Q" + CONVERT(VARCHAR, QUARTER(TheDate))
    YYYYQQ = CONCAT(YEAR(TheDate), 'Q', QUARTER(TheDate))
WHERE TheDate BETWEEN '2019-07-01' AND '2019-12-31';

